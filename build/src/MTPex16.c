#include<stdio.h>
int x;
void fun1();void fun2();void fun3();
// 0:{main,0,0}
int main()
{
	x=0;
	// (0,0)
	fun1();
	// (0,2)
	x=3;
	// (0,3)
	return 0;
}
// 1:{fun1,0,0} , 3:{fun1,4,0}
void fun1()
{
	if(x=0)
	{
		// (1,0) , (3,4)
		x=1;
		// (1,1) , (3,1)
		fun2();
		// (1,5) , (3,5)
		x=2;
		// (1,2) , (3,2)
	}
	// (1,2) , (2,2)
}
// 2:{fun2,1,0}
void fun2()
{
	if(x!=0)
	{
		// (2,0)
		x=4;
		// (2,4)
		fun1();
		// (2,2)
		x=5;
		// (2,5)
	}
	// (2,5)
}
